public class Overloading {
    public void mostrar(int i) {
        System.out.println("Se ha ingresado int");
    }

    public void mostrar(double i) {
        System.out.println("Se ha ingresado double");
    }

    public static void mostrar(String i) {
        System.out.println("Se ha ingresado String");
    }

    public static void mostrar(char i) {
        System.out.println("Se ha ingresado char");
    }
}